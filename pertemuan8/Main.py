# OPERASI ARITMATIKA

a = 10
b = 3

# Operasi Penjumlahan +
hasil = a + b
print(a," + ", b ," = ", hasil)

# Operasi Pengurangan -
hasil = a - b
print(a," - ", b ," = ", hasil)

# Operasi Perkalian *
hasil = a * b
print(a," * ", b ," = ", hasil)

# Operasi Pembagian /
hasil = a / b
print(a," / ", b ," = ", hasil)

# Operasi Exponen (pangkat) **
hasil = a ** b
print(a," ** ", b ," = ", hasil)

# Operasi Modulus % (sisa pembagian)
hasil = a % b
print(a," % ", b ," = ", hasil)

# Operasi Floor Division // (kebalikan dari modulus)
hasil = a // b
print(a," // ", b ," = ", hasil)

